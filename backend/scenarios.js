function scenarioGuide() {
  // Compact guide injected into the system prompt
  const lines = ['ДОСТУПНІ СЦЕНАРІЇ З ФОТО (вживай ТЕГ [[SCENE:key]] окремим рядком, КОЛИ доречно показати фото — і одразу після цього коротко прокоментуй):'];
  for (const [key, s] of Object.entries(SCENARIOS)) {
    lines.push(`• ${key} — ${s.title}. ${s.description} Тригери: ${s.triggers.slice(0,3).join(' / ')}.`);
  }
  lines.push('Правила: за один меседж не більше одного [[SCENE:...]]. Не показуй фото, якщо клієнтка про нього не питає або не йде про вибір принта/розміру/комплекту/упаковки.');
  return lines.join('\n');
}


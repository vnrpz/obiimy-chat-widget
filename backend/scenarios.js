function scenarioGuide() {
  const lines = ["ДОСТУПНІ СЦЕНАРІЇ З ФОТО (вживай ТЕГ [[SCENE:key]] окремим рядком, КОЛИ доречно показати фото — і одразу після цього коротко прокоментуй):"];
  for (const [key, s] of Object.entries(SCENARIOS)) {
    lines.push(`• ${key} — ${s.title}. ${s.description} Тригери: ${s.triggers.slice(0,3).join(" / ")}.`);
  }
  lines.push("Правила: за один меседж не більше одного [[SCENE:...]]. Спершу пропонуй конкретну сцену принта (mask_xxx, pillow_xxx, bookmark_xxx), якщо клієнтка назвала принт. Якщо клієнтка лише питає категорію («які маски є?») — використовуй *_catalog. Не вигадуй сцен, яких немає у списку.");
  return lines.join("\n");
}

